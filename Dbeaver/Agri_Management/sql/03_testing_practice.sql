-- 1. Create the table
CREATE TABLE crop_sales_practice (
    sale_id SERIAL PRIMARY KEY,
    crop_name VARCHAR(50),
    sale_date DATE,
    quantity_kg INT,
    price_per_kg DECIMAL(10,2),
    total_revenue DECIMAL(10,2),
    region VARCHAR(50),
    status VARCHAR(20) -- 'Completed', 'Pending', 'Cancelled'
);

